# BASE CONVERSIONS FUNCTIONS

# 1. bin() => other number systems (decimal, octal, hexadecimal) to binary
print(bin(15))
print(bin(0o132))
print(bin(0xface))

# 2. oct() => other number systems (decimal, binary, hexadecimal) to octal
print(oct(100))
print(oct(0b1111))
print(oct(0xface))

print("---------------------------------------")
# 3. hex() => other number systems (decimal, octal, binary) to hexadecimal
print(hex(1000))
print(hex(0b100111))
print(hex(0o127))